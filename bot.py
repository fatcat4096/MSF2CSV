# Console Formatter
class LoggingFormatter(logging.Formatter):

	COLORS = {
		logging.DEBUG:    ansi.ltyel,
		logging.INFO:     ansi.ltblu,
		logging.WARNING:  ansi.ltyel,
		logging.ERROR:    ansi.red,
		logging.CRITICAL: ansi.ltred,
	}

	def format(self, record):
		format = "(dkgray){asctime}(reset) (log_color){levelname:<5}(reset) (green){name}(reset) {message}"

		format = format.replace("(log_color)", self.COLORS[record.levelno])

		format = format.replace("(dkgray)", ansi.dkgray)
		format = format.replace("(reset)",  ansi.rst)
		format = format.replace("(green)",  ansi.ltgrn)
		format = format.replace("(yellow)", ansi.ltyel)
		format = format.replace("(cyan)",   ansi.ltcyan)
		format = format.replace("(red)",    ansi.ltred)

		formatter = logging.Formatter(format, "%m-%d %H:%M:%S", style="{")
		return formatter.format(record)

